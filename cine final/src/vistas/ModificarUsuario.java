    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtIngreseID.getText());
            String usuario = txtUsuario.getText();
            String pass = txtPass.getText();
            String tipousuarioseleccionado = (String) cboTipoUsuario.getSelectedItem();

            int idtipousuario = obtenerIdUsuarioDesdeNombre(tipousuarioseleccionado);

            Usuario usuarioActualizado = new Usuario(id, usuario, pass, idtipousuario);

            // Llamar al método para actualizar la película en la base de datos
            dao.updateUsuario(usuarioActualizado);

            JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Entrada no válida. Verifica los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtIngreseID.getText());
            Usuario usuario = dao.buscarUsuario(id);

            if (usuario != null) {
                llenarCamposConUsuario(usuario);
                txtUsuario.setEnabled(true);
                txtPass.setEnabled(true);
                cboTipoUsuario.setEnabled(true);
                btnActualizar.setEnabled(true);
                JOptionPane.showMessageDialog(this, "Usuario encontrado!");
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrada. Verifica el ID.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // Manejar el caso en el que la entrada no es un entero válido
            JOptionPane.showMessageDialog(this, "Error: Entrada no válida. Por favor, ingresa un ID válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

