    private void llenarCamposConUsuario(Usuario usuario) {
        txtUsuario.setText(String.valueOf(usuario.getUsuario()));
        txtPass.setText(String.valueOf(usuario.getPass()));

        String a = null;
        if(usuario.getTipo_usuario()==1){
            a = "Admin";
        }else if(usuario.getTipo_usuario()==2){
            a = "Usuario";
        }
        cboTipoUsuario.setSelectedItem(a);
    }  
