    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
        // Obtener los valores de los campos de texto
        int id = Integer.parseInt(txtIngreseId.getText());
        int anio = Integer.parseInt(txtAnio.getText());
        int duracion = Integer.parseInt(txtDuracion.getText());
        String titulo = txtTitulo.getText();
        String director = txtDirector.getText();
        String generoSeleccionado = (String) cboGenero.getSelectedItem();

        // Realizar la conversión del nombre del género al código numérico
        int idGenero = obtenerIdGeneroDesdeNombre(generoSeleccionado);

        // Crear una nueva instancia de Pelicula con los nuevos datos
        Pelicula peliculaActualizada = new Pelicula(id, anio, duracion, titulo, director, idGenero);

        // Llamar al método para actualizar la película en la base de datos
        dao.updatePelicula(peliculaActualizada);

        JOptionPane.showMessageDialog(this, "Pelicula actualizada correctamente!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Entrada no válida. Verifica los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed
    
    private int obtenerIdGeneroDesdeNombre(String nombreGenero) {
        // Implementa lógica para obtener el ID del género desde el nombre
        // Puedes tener una lista de géneros o una consulta a la base de datos para realizar la correspondencia
        // Este es solo un ejemplo, asegúrate de adaptarlo a tu estructura de datos
        if ("Drama".equals(nombreGenero)) {
            return 2; // Por ejemplo, asigna 1 a Drama
        } else if ("Terror".equals(nombreGenero)) {
            return 1; // Por ejemplo, asigna 2 a Terror
        } else if ("Ficcion".equals(nombreGenero)) {
            return 3; // Por ejemplo, asigna 3 a Ficción
        } else {
            // Maneja casos donde el género no coincide
            return -1; // O lanza una excepción, dependiendo de tu lógica
        }
    }
