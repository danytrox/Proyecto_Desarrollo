    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int id = Integer.parseInt(txtIngreseId.getText());
            Pelicula pelicula = dao.buscarPelicula(id);

            if (pelicula != null) {
                // Llamar al método para rellenar campos
                llenarCamposConPelicula(pelicula);
                txtAnio.setEnabled(true);
                txtDuracion.setEnabled(true);
                txtTitulo.setEnabled(true);
                txtDirector.setEnabled(true);
                cboGenero.setEnabled(true);
                btnActualizar.setEnabled(true);
                JOptionPane.showMessageDialog(this, "Pelicula encontrada!");
            } else {
                JOptionPane.showMessageDialog(this, "Pelicula no encontrada. Verifica el ID.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            // Manejar el caso en el que la entrada no es un entero válido
            JOptionPane.showMessageDialog(this, "Error: Entrada no válida. Por favor, ingresa un ID válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
}

// Nuevo método para rellenar campos con datos de una película
    private void llenarCamposConPelicula(Pelicula pelicula) {
        txtAnio.setText(String.valueOf(pelicula.getAnio()));
        txtDuracion.setText(String.valueOf(pelicula.getDuracion()));
        txtTitulo.setText(pelicula.getTitulo());
        txtDirector.setText(pelicula.getDirector());
        
        
        String a = null;
        if(pelicula.getGenero()==1){
            a = "Terror";
        }else if(pelicula.getGenero()==2){
            a = "Drama";
        }else if(pelicula.getGenero()==3){
            a = "Ficcion";
        }
        cboGenero.setSelectedItem(a);
    }//GEN-LAST:event_btnBuscarActionPerformed



